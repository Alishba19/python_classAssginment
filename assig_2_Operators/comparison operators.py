# ASSIGNMENT COMPARISON OPERATOR IN PYTHON:
# These are used to compare two values.
# They always return True or False as the result.

# ---------------------------
# LESS THAN (<)
a = 15
b = 5
print(a < b)       # Output: False (15 is not less than 5)

# LESS THAN OR EQUAL TO (<=)
a = 20
b = 15
print(a <= b)     # Output: False (20 is not less than or equal to 15)

# GREATER THAN OR EQUAL TO (>=)
a = 15
b = 40
print(a >= b)     # Output: False (15 is not greater than or equal to 40)


# EQUAL TO (==)
a = 20
b = 20
print( a == b)     # Output: True (Both values are equal)

# NOT EQUAL TO (!=)
a = 5
b = 15
print(a != b)     # Output: True (Values are not equal)

# GREATER THAN (>)
a = 20
b = 10
print(a > b)       # Output: True (20 is greater than 10)

