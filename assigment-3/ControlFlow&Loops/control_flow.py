# ----------------------------------------
# CONTROL FLOW STATEMENTS (if, if-else, if-elif-else)
# ----------------------------------------

# 1) Simple if statement
num = 10 
if num > 0:
    print("1) This is a positive number.")

# 2) if-else Statement 
age = 17 
if age >= 18:
    print("2) You are an adult.")

# 3) if-else-else Statement
score = 72
if score >= 90:
    print( "3) Grade: A")
elif score >= 70:
    print("3) Grade: B")
else: 
    print( "3) Grade: C or below")        